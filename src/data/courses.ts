export const findCoursesByCategory = (categoryId: string): CourseType[] => {
  return courses.filter(course => course.categoryId === categoryId);
};

export const findCategoryById = (categoryId: string): CategoryType | undefined => {
  return categories.find(category => category.id === categoryId);
};

export const findCourseById = (courseId: string): CourseType | undefined => {
  return courses.find(course => course.id === courseId);
};
